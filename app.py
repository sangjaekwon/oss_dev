# ---- 데이터 로딩 ----
@st.cache_data
def load_data():
    # 인코딩 자동 감지
    with open("accident_stats.csv", "rb") as f:
        result = chardet.detect(f.read())
        encoding = result["encoding"]
    
    df = pd.read_csv("accident_stats.csv", encoding=encoding)
    
    # 컬럼명 전처리 (여기서는 공백 제거)
    df.columns = df.columns.str.strip()
    return df

